package cn.edu.sdu.cs.starry.taurus.common.exception;
/**
 * 
 * @author SDU.xccui
 *
 */
public class BusinessCorrespondingException extends BusinessException {

	private static final long serialVersionUID = 1265456719837512298L;

	public BusinessCorrespondingException() {
		super();
	}

	public BusinessCorrespondingException(Throwable cause) {
		super(cause);
	}

	public BusinessCorrespondingException(String message) {
		super(message);
	}

	public BusinessCorrespondingException(String message, Throwable cause) {
		super(message, cause);
	}

}
